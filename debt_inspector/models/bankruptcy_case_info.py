def get_sro_for_region(region: str) -> list[dict]:
    """Возвращает список СРО, работающих в указанном регионе.
    Сначала СРО, у которых регион в приоритете, потом остальные.
    """
    region_lower = region.lower()
    priority = []
    other = []
    for sro in SRO_LIST:
        matched = any(r.lower() in region_lower or region_lower in r.lower() for r in sro["regions"])
        if matched:
            priority.append(sro)
        else:
            other.append(sro)
    return priority + other


