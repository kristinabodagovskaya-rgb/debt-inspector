def determine_route(total_debt: float) -> BankruptcyRoute:
    """Определяет маршрут банкротства по сумме долга.

    С 01.11.2024 лимит МФЦ увеличен до 1 000 000 руб.
    Судебное банкротство — при долге от 500 000 руб.
    Диапазон 500к-1М — подходят оба маршрута, приоритет МФЦ (бесплатно).
    """
    if total_debt <= 0:
        return BankruptcyRoute.UNKNOWN
    if total_debt < 25_000:
        return BankruptcyRoute.UNKNOWN
    if 25_000 <= total_debt <= 1_000_000:
        return BankruptcyRoute.MFC
    return BankruptcyRoute.COURT


def check_mfc_eligibility(
    total_debt: float,
    ip_ended_art46: bool = False,
    ip_longer_7_years: bool = False,
    is_pensioner: bool = False,
    receives_benefits: bool = False,
) -> dict:
    """Проверяет право на внесудебное банкротство через МФЦ.

    Условия (ст. 223.2 ФЗ-127, ред. от 01.11.2024):
    - Долг от 25 000 до 1 000 000 руб.
    - ИП окончено по п.4 ч.1 ст.46 (нет имущества)
      ИЛИ ИП длится >7 лет
      ИЛИ спец. категории (пенсионер, получатель пособия)
    """
    result = {
        "eligible": False,
        "amount_ok": 25_000 <= total_debt <= 1_000_000,
        "has_basis": False,
        "basis": "",
        "reasons": [],
    }

    if not result["amount_ok"]:
        if total_debt < 25_000:
            result["reasons"].append("Сумма долга менее 25 000 руб.")
        else:
            result["reasons"].append("Сумма долга превышает 1 000 000 руб.")
        return result

    if ip_ended_art46:
        result["has_basis"] = True
        result["basis"] = "ИП окончено по п.4 ч.1 ст.46 ФЗ-229 (отсутствие имущества)"
    elif ip_longer_7_years:
        result["has_basis"] = True
        result["basis"] = "ИП длится более 7 лет"
    elif is_pensioner:
        result["has_basis"] = True
        result["basis"] = "Получатель пенсии (основной доход — пенсия)"
    elif receives_benefits:
        result["has_basis"] = True
        result["basis"] = "Получатель пособия (единственный доход — пособие)"
    else:
        result["reasons"].append(
            "Нет оснований для МФЦ: нужно окончание ИП по ст. 46 "
            "или статус пенсионера/получателя пособий"
        )

    result["eligible"] = result["amount_ok"] and result["has_basis"]
    return result


